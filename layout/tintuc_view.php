  <div class="showText">
    <p>Nhà thờ Hạnh Thông Tây toạ lạc tại số 53/7, đường Quang Trung, phường 11, quận Gò Vấp, Tp. Hồ Chí Minh trong dáng vẻ nhẹ nhàng, thanh thoát với gam màu trắng, xám chủ đạo. Đây là một công trình hiếm hoi theo phong cách kiến trúc Byzantine, trong khi hầu hết nhà thờ ở Việt Nam được thiết kế theo phong cách Gothique hoặc Romanesque. Điểm riêng biệt của kiến trúc Byzantine ở nhà thờ thể hiện qua tháp tròn có mái vòm đúc hình bán cầu duy nhất phía cuối gần cung thánh (kiểu mặt bằng tập trung). Bên trên mái vòm là tháp nhỏ hình chóp nhọn để lấy ánh sáng. Tường ngoài nhà thờ là những mảng trơn được đắp gờ, chỉ trang trí kết hợp hoa văn, phù điêu đơn giản bằng thạch cao. Phía dưới tháp chuông nhà thờ được xây bằng đá tảng, bên trong là một bộ chuông gồm ba cái mang ba âm khác nhau. Bộ chuông này được hãng đúc chuông nổi tiếng Paccard của Pháp đúc vào năm 1925. </p>
    <p><img src="delete/4.jpg" width="1200" height="800" /></p>
    <p>Khác với bề ngoài trông đơn giản, nội thất nhà thờ được trang hoàng lộng lẫy thể hiện qua những chi tiết trang trí tỉ mỉ, cầu kỳ. Nổi bật là mái vòm cung tròn dọc theo chiều dài nhà thờ được ghép từ những phù điêu hoa văn hình vuông. Dọc hai bên là hàng cột với hoạ tiết tinh xảo trên phần đầu. Giữa các cột cũng sử dụng hình thức kết cấu dạng vòm, treo những chiếc quạt trần từ thời Pháp. Xen kẽ giữa những ô cửa sổ kính màu sặc sỡ là những bức phù điêu thếp vàng óng ánh phác hoạ lại từng chặng đường khổ nạn mà Chúa Giesu đã trải qua. Đặc biệt hơn cả là ba bàn thờ trên cung thánh được điêu khắc rất công phu từ đá cẩm thạch vàng của Ý. Xung quanh tường và trần được trang trí bằng rất nhiều bức tranh khảm gạch (theo trường phái Mosaic). Ấn tượng nhất là bức bích họa hình Chúa Giesu đang trăn trối bên Đức mẹ Maria và Thánh Gioan ở vòm cung thánh. </p>
    <p>Nhà thờ được xây dựng trong 3 năm (1921-1924) do ông Lê Phát An đầu tư cho hai nhà thầu Baader và Lamorte của Pháp thi công. Ông Lê Phát An là cậu ruột của hoàng hậu Nam Phương (vợ vua Bảo Đại, vị hoàng đế cuối cùng của triều đình phong kiến Việt Nam) và là con của ông Lê Phát Đạt, một trong bốn đại điền chủ nổi tiếng nhất Nam Bộ lúc bấy giờ. Để tỏ lòng biết ơn, cộng đoàn đã cho an táng hai vợ chồng ông ngay trong lòng thánh đường. Trước mỗi phần mộ là tượng hai ông bà bằng đá cẩm thạch trong trang phục áo dài truyền thống đang quỳ gối dâng hoa được điêu khắc vô cùng sống động do kiến trúc sư nổi tiếng người Pháp là A. Contenay và P. Ducuing thực hiện. Trong khuôn viên nhà thờ, vào mỗi buổi chiều sau giờ hành lễ, Tòa Thánh Giuse là nơi mọi người gửi gắm tư tâm, vui buồn trong cuộc sống, Toà Đức Mẹ Maria là nơi cầu nguyện của những người đi đạo… </p>
    <p>Với Giáo xứ Hạnh Thông Tây tồn tại hơn 150 năm, nhà thờ Hạnh Thông Tây để lại dấu ấn sâu sắc trong hành trình đức tin của gần 6000 giáo dân nơi đây. Trải qua bao thăng trầm của thời gian, nhà thờ Hạnh Thông Tây đã trở thành tài sản quý giá về mặt tôn giáo và là một di sản kiến trúc độc đáo của Sài thành. </p>
